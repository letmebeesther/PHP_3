<?php // [예제 2-3] 상수와 변수 사용하기

 	$a = 10;
	$b = 20;
	$c = $a+$b;				//변수 a와 변수 b 정수 합계
	echo "$c <br>";		//합계를 화면에 출력하고, 엔터

	$a = 18.5;
	$b = 37.3;
	$c = $a+$b;				//변수 a와 변수 b 실수 합계
	echo "$c <br>";				//합계를 화면에 출력하고, 엔터

	$fruit = "사과";
	echo "$fruit <br>";		//변수 값(사과)을 화면에 출력하고, 엔터

	$fruit = "오렌지";
	echo "$fruit";	//변수 값(오렌지)을 화면에 출력하고, 엔터
?>